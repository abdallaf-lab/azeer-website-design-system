export const DropdownMenuContent = React.forwardRef<
  React.ElementRef<typeof DropdownMenuPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.Content>
>(({ className, sideOffset = 4, ...props }, ref) => (
  <DropdownMenuPrimitive.Portal>
    <DropdownMenuPrimitive.Content
      ref={ref}
      sideOffset={sideOffset}
      className={cn(
        'z-[var(--z-dropdown)] min-w-[200px] overflow-hidden',
        'bg-[var(--color-bg-surface)] rounded-[var(--radius-md)] shadow-[var(--shadow-elev-2)]',
        'p-1',
        'data-[state=open]:animate-in data-[state=closed]:animate-out',
        'data-[state=open]:fade-in-0 data-[state=closed]:fade-out-0',
        'data-[state=open]:zoom-in-95 data-[state=closed]:zoom-out-95',
        className,
      )}
      {...props}
    />
  </DropdownMenuPrimitive.Portal>
));
DropdownMenuContent.displayName = 'DropdownMenuContent';

export const DropdownMenuItem = React.forwardRef<
  React.ElementRef<typeof DropdownMenuPrimitive.Item>,
  React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.Item> & {
    intent?: 'default' | 'danger';
  }
>(({ className, intent = 'default', ...props }, ref) => (
  <DropdownMenuPrimitive.Item
    ref={ref}
    className={cn(
      'relative flex cursor-pointer select-none items-center gap-2',
      'rounded-[var(--radius-sm)] px-2 py-1.5 text-[13px] leading-4 outline-none',
      'transition-colors duration-100',
      'data-[disabled]:pointer-events-none data-[disabled]:opacity-50',
      "[&_svg]:size-4 [&_svg]:text-[var(--color-fg-muted)]",
      intent === 'default' &&
        'text-[var(--color-fg-default)] hover:bg-[var(--color-state-hover)] focus:bg-[var(--color-state-hover)]',
      intent === 'danger' &&
        'text-[var(--color-danger-text)] hover:bg-[var(--color-danger-bg-subtle)] focus:bg-[var(--color-danger-bg-subtle)] [&_svg]:text-[var(--color-danger-text)]',
      className,
    )}
    {...props}
  />
));
DropdownMenuItem.displayName = 'DropdownMenuItem';

