export type StringKey = keyof typeof STRINGS.en;

export function translate(
  key: StringKey,
  locale: Locale,
  params?: Record<string, string | number>,
): string {
  // Fall back to English if the AR catalogue is missing a key (helps when
  // we add new strings to en/ but haven't translated yet — never renders
  // a raw key).
  let s: string = (STRINGS[locale] as Record<string, string>)[key]
    ?? STRINGS.en[key]
    ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return s;
}
